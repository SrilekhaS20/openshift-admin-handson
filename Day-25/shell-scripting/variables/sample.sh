#!/bin/bash
# Below are some examples of predefined or system variables in bash
echo $BASH # Displays the path of the bash shell
echo $HOME # Displays the home directory of the current user
echo $PWD # Displays the present working directory
echo $BASH_VERSION # Displays the version of the bash shell

# Below is an example of a user-defined variable in bash
name=Sri
echo "Hello, $name" # Displays Hello, Sri